import Head from 'next/head'
import { useRouter } from 'next/router'
import { useState, useContext } from 'react'
import AuthContext from '@/context/AuthContext'
import MainLayout from '@/components/layout/MainLayout'
import { API_URL } from '@/config/index'
import { parseCookies } from '@/helpers/index'
import { Switch } from '@headlessui/react'
import Link from 'next/link'
import toast, { Toaster } from 'react-hot-toast'
import ReqDetailsRequestor from '@/components/each-item/ReqDetailsRequestor'
import { FiAlertTriangle, FiMap, FiPhone } from 'react-icons/fi'

export default function RequestPage({ item, request, token }) {
  const [enabledAccept, setEnabledAccept] = useState(request.accepted)
  const [enabledDelivered, setEnabledDelivered] = useState(request.delivered)

  const router = useRouter()
  const { user } = useContext(AuthContext)

  const handleSubmit = async (e) => {
    e.preventDefault()

    // SET REQUEST AS ACCEPTED OR DELIVERED

    const res = await fetch(
      `${API_URL}/requests/${request.id}/${request.item.user}`,
      {
        method: 'PUT',
        headers: {
          'Content-Type': 'application/json',
          Authorization: `Bearer ${token}`,
        },
        body: JSON.stringify({
          accepted: enabledAccept,
          delivered: enabledDelivered,
        }),
      },
    )

    // SET ITEM AS DELIVERED OR ACCEPTED

    const result = await fetch(`${API_URL}/items/${item.id}`, {
      method: 'PUT',
      headers: {
        'Content-Type': 'application/json',
        Authorization: `Bearer ${token}`,
      },
      body: JSON.stringify({
        accepted: enabledAccept,
        delivered: enabledDelivered,
      }),
    })

    // CHECK FOR ERRORS DURING THE API CALL

    if (!res.ok || !result.ok) {
      toast.error('Something went wrong. Try again.')
    } else {
      toast.success('Changes successful.')
      setTimeout(function () {
        router.reload()
      }, 3000)
    }
  }

  console.log(request)

  return (
    <MainLayout>
      <Head>
        <title>{`Request`} </title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <section className="text-gray-600 dark:text-gray-400 body-font overflow-hidden">
        <div className="container px-3 py-6 mx-auto max-w-screen-xl mt-6 min-h-full">
          <Toaster position="top-center" reverseOrder={false} />
          <div className="lg:w-1/2 md:w-2/3 mx-auto sm:border border-gray-200 sm:px-10 sm:py-5 sm:rounded-md mb-6 sm:bg-white dark:bg-gray-800 dark:border-gray-700">
            <h1 className="text-xl text-gray-700 font-semibold mt-2 uppercase dark:text-white">{`Request for ${request?.item?.name}`}</h1>
            <p className="text-gray-500 font-normal text-xs tracking-wider mb-2 dark:text-gray-400">
              {`By ${request?.requested_by?.username}`}
            </p>
            <p className="mb-1 text-gray-800 dark:text-white">
              {request?.description}
            </p>
            <div className="mb-1 text-gray-800 dark:text-white mt-2 flex items-center">
              <FiMap className="mr-3 text-emerald-700 dark:text-emerald-400" />
              <p className="text-sm font-normal text-gray-600 dark:text-gray-400">
                {request?.location}
              </p>
            </div>
            <div className="mb-1 text-gray-800 dark:text-white flex items-center">
              <FiPhone className="mr-3 text-emerald-700 dark:text-emerald-400" />
              <p className="text-sm font-normal text-gray-600 dark:text-gray-400">
                {request?.phone_number}
              </p>
            </div>
            <div className="mb-1 text-gray-800 dark:text-white flex items-center">
              <FiPhone className="mr-3 text-emerald-700 dark:text-emerald-400" />
              <p className="text-sm font-normal text-gray-600 dark:text-gray-400">
                {request?.phone_number}
              </p>
            </div>
          </div>

          {/* CHECK IF THE LOGGED IN USER IS THE ITEM OWNER */}

          {request.item.user === user?.id && (
            <form
              className="lg:w-1/2 md:w-2/3 mx-auto sm:border sm:rounded-md border-gray-200 mb-4 sm:bg-white dark:bg-gray-800 dark:border-gray-700"
              onSubmit={handleSubmit}
            >
              {/* CHECK IF ITEM IS ACCEPTED AND REQUEST IS ACCEPTED OTHERWISE SHOW ALERT */}

              {(request.accepted === false && item.accepted === false) ||
              (request.accepted === true && item.accepted === true) ? (
                <div className="md:mx-6 md:px-4 md:py-1 mt-6 pb-4 font-semibold uppercase text-gray-800 dark:text-white">
                  <h2 className="sm:pb-4">Mark as accepted or delivered</h2>
                </div>
              ) : (
                <div className="md:mx-6 md:px-4 md:py-3 md:my-4 m-4 font-semibold  text-gray-600 dark:text-white">
                  <div className="flex items-center">
                    <FiAlertTriangle className="text-emerald-600 dark:text-emerald-400 text-6xl mr-3" />
                    <h2 className="text-gray-600 dark:text-emerald-600">
                      If you want to accept this request you must not accept any
                      other request.
                    </h2>
                  </div>
                </div>
              )}

              {/* CHECK IF ITEM IS NOT ACCEPTED AND REQUEST IS NOT ACCEPTED BEFORE SHOWING THE CARD */}

              {(request.accepted === false && item.accepted === false) ||
                (request.accepted === true && item.accepted === true && (
                  <div className="md:mx-6 md:px-4 md:py-1">
                    <div className="flex items-center justify-between">
                      <div className="flex-grow flex flex-col p-2 mr-6">
                        <h2 className="mr-4 text-base font-semibold text-gray-600 dark:text-white">
                          Accept this request
                        </h2>
                        <p className="text-sm text-gray-500 dark:text-gray-400">
                          You will not accept other requests.
                        </p>
                      </div>

                      <div
                        onClick={() => setEnabledAccept(!enabledAccept)}
                        className={`${
                          enabledAccept
                            ? 'bg-emerald-600'
                            : 'bg-gray-200 dark:bg-gray-500'
                        } relative inline-flex items-center h-6 rounded-full w-11 transition-colors focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-emerald-500`}
                      >
                        <span
                          className={`${
                            enabledAccept ? 'translate-x-6' : 'translate-x-1'
                          } inline-block w-4 h-4 transform bg-white dark:bg-gray-900 rounded-full transition-transform`}
                        />
                      </div>
                    </div>
                  </div>
                ))}

              {request.accepted === false && item.accepted === false && (
                <div className="md:mx-6 md:px-4 md:py-1">
                  <div className="flex items-center justify-between">
                    <div className="flex-grow flex flex-col p-2 mr-6">
                      <h2 className="mr-4 text-base font-semibold text-gray-600 dark:text-white ">
                        Accept this request
                      </h2>
                      <p className="text-sm text-gray-500 dark:text-gray-400">
                        You will not accept other requests.
                      </p>
                    </div>

                    <div
                      onClick={() => setEnabledAccept(!enabledAccept)}
                      className={`${
                        enabledAccept
                          ? 'bg-emerald-600'
                          : 'bg-gray-200 dark:bg-gray-500'
                      } relative inline-flex items-center h-6 rounded-full w-11 transition-colors focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-emerald-500`}
                    >
                      <span
                        className={`${
                          enabledAccept ? 'translate-x-6' : 'translate-x-1'
                        } inline-block w-4 h-4 transform bg-white dark:bg-gray-900 rounded-full transition-transform`}
                      />
                    </div>
                  </div>
                </div>
              )}

              {/* CHECK IF THE ITEM IS ACCEPTED FIRST BEFORE SHOWING THE DELIVERED SWITCH */}

              {request.accepted === true && item.accepted === true && (
                <div className="md:mx-6 md:px-4 md:py-1">
                  <Switch.Group
                    as="div"
                    className="flex items-center justify-between"
                  >
                    <div className="flex-grow flex flex-col p-2 mr-6">
                      <h2 className="mr-4 text-base font-semibold text-gray-600 dark:text-white">
                        Mark as delivered
                      </h2>
                      <p className="text-sm text-gray-500 dark:text-gray-400">
                        Set the Item to be delivered.
                      </p>
                    </div>

                    <div
                      onClick={() => setEnabledDelivered(!enabledDelivered)}
                      className={`${
                        enabledDelivered
                          ? 'bg-emerald-500'
                          : 'bg-gray-200 dark:bg-gray-500'
                      } relative inline-flex items-center h-6 rounded-full w-11 transition-colors focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-emerald-500`}
                    >
                      <span
                        className={`${
                          enabledDelivered ? 'translate-x-6' : 'translate-x-1'
                        } inline-block w-4 h-4 transform bg-white dark:bg-gray-800 rounded-full transition-transform`}
                      />
                    </div>
                  </Switch.Group>
                </div>
              )}

              {/* CHECK IF ITEM IS ACCEPTED AND REQUEST IS ACCEPTED OR IF ITEM AND REQUEST ARE NOT ACCEPTD OTHERWISE DO NOT SHOW BUTTONS */}

              {(request.accepted === false && item.accepted === false) ||
                (request.accepted === true && item.accepted === true && (
                  <div className="md:mx-6 md:px-4 md:py-1 my-4">
                    <Link href="#">
                      <a className="text-gray-600 border border-gray-200 py-3 w-full px-5 mr-2 rounded-sm">
                        Cancel
                      </a>
                    </Link>
                    <button
                      className="text-gray-600 font-semibold bg-emerald-400 py-3 px-5 rounded-sm"
                      type="submit"
                    >
                      Confirm Changes
                    </button>
                  </div>
                ))}

              {request.accepted === false && item.accepted === false && (
                <div className="md:mx-6 md:px-4 md:py-1 my-4">
                  <Link href="#">
                    <a className="text-gray-600 border border-gray-200 py-3 w-full px-5 mr-2 rounded-sm dark:text-gray-400 dark:border-gray-700">
                      Cancel
                    </a>
                  </Link>
                  <button
                    className="text-gray-50 font-semibold bg-emerald-700 py-3 px-5 rounded-sm dark:text-white dark:bg-emerald-600"
                    type="submit"
                  >
                    Confirm Changes
                  </button>
                </div>
              )}
            </form>
          )}

          {/* SHOWING WHETHR REQUEST IS ACCEPTED OR DELIVERED TO THE REQUESTOR OF THE ITEM */}

          {request?.requested_by?.id === user?.id && (
            <ReqDetailsRequestor request={request} item={item} />
          )}
        </div>
      </section>
    </MainLayout>
  )
}

export async function getServerSideProps({ req, params: { slug, requestId } }) {
  const { token } = parseCookies(req)

  if (!token) {
    return {
      redirect: {
        destination: '/login',
        permanent: false,
      },
    }
  }

  const reqs = await fetch(`${API_URL}/requests/${requestId}`, {
    method: 'GET',
    headers: {
      Authorization: `Bearer ${token}`,
    },
  })

  const res = await fetch(`${API_URL}/items/${slug}`, {
    method: 'GET',
    headers: {
      Authorization: `Bearer ${token}`,
    },
  })

  const request = await reqs.json()
  const item = await res.json()

  return {
    props: {
      token,
      item,
      request,
    },
  }
}
